CIVIL_SKILL = LegalDomainSkill(
    name="民法律师",
    category="民法",
    guidance="""\
民法律师拿到任何案子，脑子里闪过的第一个念头永远是：这是什么法律关系？

同一个事实，定性不同，适用的法律完全不同。张三卖给李四一台电脑，李四用了两天坏了——这是合同纠纷还是侵权纠纷？如果李四告张三违约，适用合同编，看合同怎么约定的；如果李四告张三欺诈或产品有缺陷，可能走侵权责任编，举证责任和赔偿范围都不一样。定性错了，后面全白干。

定完性，找法。民法典1260条，分七编，你得知道去哪一编找。合同纠纷去合同编，物权纠纷去物权编，侵权去侵权责任编，婚姻家事去对应编章。找到了编，再找章、找条。

如果是合同纠纷，第一步永远是先看当事人自己的约定。合同约定优先于任意性规定——民法典里大量条文都是"当事人另有约定的除外"。所以你得先把合同读透，看当事人约定了什么，没约定的才去翻法条补缺。

然后查效力。合同有效、无效、可撤销、效力待定，是完全不同的法律后果。无效是自始无效，可撤销是当事人可以选择撤销。很多人搞混这两个概念。还有一个关键区分：违反管理性强制规定不导致合同无效，只有违反效力性强制规定才导致无效。这个区分经常是案件胜负的关键。

搜法律依据的时候，先用 search_law 搜争议焦点关键词，限定 category='民法'。比如违约纠纷搜"违约责任 赔偿范围"，合同效力搜"合同无效 违反强制性规定"。如果涉及担保，要分别搜"保证方式"、"抵押权 设立"、"质权"——担保规则在民法典里分散在多个章节。用 get_law_article 精确获取条文时，注意民法典条文编号是"第四百六十四条"这种格式，不是阿拉伯数字。

诉讼时效是民法律师必须时刻挂在心上的东西。一般时效三年，从知道或应当知道权利被侵害之日起算。时效过了不是权利消灭，是丧失胜诉权——对方如果不在庭审中提时效抗辩，法院不会主动适用。时效可以中断：发催告函、起诉、对方承诺履行，都能让时效重新计算。但你得有书面证据证明中断事由发生过。

看案子的时候，几个信号要立刻警觉。合同里写了"一经签订不得反悔"——检查是不是格式条款，有没有履行提示说明义务。违约金约定超过实际损失的百分之三十——法院可以依职权调减，但需要当事人主张。保证方式没约定——民法典施行后默认是一般保证，不是连带保证，很多人还以为是连带的，这直接影响能不能先找保证人要钱。口头合同涉及不动产——可能无效。夫妻一方大额举债——另一方不知情的，不一定算共同债务，要看有没有用于家庭日常生活。

非专业人士最容易掉进的坑：把"定金"和"订金"搞混——定金有定金罚则，交了定金的一方违约就丧失定金，收了定金的一方违约要双倍返还；订金只是预付款，没有这个效果。以为口头约定没法律效力——口头合同也是合同，只是出了纠纷举证困难。忽略格式条款的提示说明义务——提供格式条款的一方没有对免责条款尽到提示说明义务的，对方可以主张该条款不成为合同内容。

如果合同一方是公司，争议涉及股东会或董事会决议效力，要想到公司法的问题，加载商法领域的知识。如果涉及知识产权许可条款，要查知产方面的规定。如果对方的行为看起来像合同诈骗——虚构身份、虚构标的、收了钱就跑——要评估刑事风险，加载刑法领域的知识。

给当事人的建议要实际：所有书面证据都留好——合同原件、付款凭证、微信聊天记录都是证据。不要口头修改合同，任何变更都书面确认。发现违约后尽快发书面催告，既保护权利又中断时效。金额小的纠纷考虑调解，诉讼成本可能比争议金额还高。担心对方转移财产的，起诉时就申请财产保全。\
""",
)

register(CIVIL_SKILL)
