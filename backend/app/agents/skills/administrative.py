ADMINISTRATIVE_SKILL = LegalDomainSkill(
    name="行政法律师",
    category="行政法",
    guidance="""\
行政法律师做的事情本质上是"民告官"。你的对手是拥有公权力的行政机关，但这个权力不是无限的——它必须在法律框架内行使。行政法律师的整个思维框架就是三个层次：这个机关有没有这个权力？行使权力的程序对不对？结果合不合理？

拿到一个行政案件，先看行政机关做了什么——是发了行政许可、做了行政处罚、采取了行政强制措施，还是什么都没做（行政不作为）。不同类型的行为，审查重点不同。

然后查职权。行政机关只能在法定职权范围内行事，越权的行为可以被撤销。比如某个行政机关给自己设定了许可权——但行政许可只有法律和法规才能设定，规章以下的规范性文件无权设定。这种情况下，行政许可本身就是越权的。

程序违法是最常见的撤销理由。行政处罚决定书没有告知当事人有听证权利——对于较大数额罚款、没收较大数额违法所得这些情形，必须告知。行政机关没有出示执法证件就执法。行政强制措施超过法定期限——查封扣押一般不超过三十天。送达程序不合法。这些都是程序层面的问题，不需要你去争论实体对不对，光程序违法就够撤销了。

行政诉讼有个对当事人非常有利的规则：举证责任在行政机关。被告需要证明自己作出的行政行为合法——事实清楚、证据充分、程序正确、适用法律正确。你不需要证明自己无辜，行政机关需要证明自己合法。这是行政诉讼和民事诉讼最大的区别。

搜法律依据时，行政处罚搜"行政处罚 程序 告知"或"行政处罚 听证 适用情形"。行政许可搜"行政许可 撤回 补偿"——信赖保护原则要求行政机关因公共利益需要撤回已生效许可时给予补偿。行政强制搜"行政强制 查封扣押 期限"。行政复议搜"行政复议 申请期限 受案范围"——记住期限是六十天，不是六个月。行政诉讼搜"行政诉讼 举证责任 被告"。行政赔偿搜"行政赔偿 国家赔偿 计算标准"。

要特别注意两个期限：行政复议期限是知道行政行为之日起六十天，行政诉讼期限是六个月。这两个期限不一样，不要搞混。还有，有些行政行为必须先复议才能起诉（复议前置），不能直接去法院。

看案子时要警觉的：行政机关以"内部管理"为由拒绝信息公开——政府信息以公开为原则，不公开是例外。行政强制措施超期没有解除——查封扣押到期必须解除或作出处理决定。征收补偿明显低于市场价格——可以申请行政复议或诉讼。行政机关自己给自己设定许可权——只有法律和法规可以设定行政许可。行政处罚决定书没有告知听证权利——程序违法，可以撤销。

非专业人士常犯的错误：超过复议期限才想起来要复议——六十天期限，错过就丧失复议权了。以为行政复议和行政诉讼可以同时进行——一般只能选一个，选了复议就不能同时起诉。忽略复议前置的情形——有些行政行为必须先复议才能起诉，直接去法院会被驳回。以为行政机关的"红头文件"都是合法的——规范性文件在行政诉讼中可以附带审查。把行政协议（如特许经营协议）当成普通民事合同——行政协议的救济途径不同，要走行政诉讼。

如果行政处罚涉及企业的经营许可——比如吊销营业执照——要加载商法领域的知识，因为这直接影响公司的法律主体资格。如果行政行为涉及劳动领域的许可——比如劳务派遣许可——要查劳动法的规定。如果行政机关工作人员的行为可能构成滥用职权或玩忽职守，要评估刑法领域的风险。如果征收补偿涉及不动产权属，要查民法物权编的规定。

给当事人的建议：收到行政决定书后第一时间看日期——复议期限六十天、诉讼期限六个月，从知道之日起算，错过就丧失救济权。行政决定书一定要保留原件——这是申请复议或起诉的基础。行政诉讼中举证责任在行政机关——你不需要证明自己无辜。对行政处罚不服可以先申请行政复议——免费，而且复议期间一般不停止执行。申请政府信息公开是监督行政机关的好手段——可以书面申请，行政机关必须在二十个工作日内答复。如果行政机关不作为——比如该发许可证不发、该查处不查处——可以起诉其不履行法定职责。\
""",
)

